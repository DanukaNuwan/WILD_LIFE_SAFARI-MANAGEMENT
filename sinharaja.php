            <p>
            The apex of rainforests in Sri Lanka and home to 95% of the endemic birds in the country, Sinharaja is a globally important biodiversity venue with a highly unique cultural significance.
            Among its many recognitions, the rainforest has been designated as a UNESCO Natural World Heritage Site, UNESCO Biosphere Reserve and also a Biodiversity Hotspot by the International Union for Conservation of Nature (IUCN). It is a sought after by wildlife researchers as well as bird watchers from all parts of the world due to its richness in biodiversity and habitat complexity.
            This lowland evergreen rain forest is adorned with lush vegetation, ample water falls and a perfect climate. The name Sinaharaja which translates to Lion (Sinha) King (Raja) makes it a revered location by Sri Lankans since it reflects the popular belief about the origin of the Sinhala people. According to the legend, the inception of the race is attributed to a union between a lion king and a princess who have sought refuge in the forest.

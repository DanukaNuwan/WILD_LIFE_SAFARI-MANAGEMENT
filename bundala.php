            <p>
            Bundala National Park is situated in the Hambantota District towards the Southern Coast of Sri Lanka. The park consists of five shallow brackish lagoons that are interconnected by channels and marshes as well as an adjacent coast. The park is an important location as the last refuge of the greater flamingo in this part of the island.
            This aquatic habitat welcomes a range of flying winter visitors who seek refuge in the park for feeding and breeding on an annual basis. Every water bird in Sri Lanka can be observed in this park making it a spectacular destination for bird lovers. The sand dunes on the beaches are visited by nesting sea turtles. In addition, the park is home to a range of threatened reptiles and mammals including elephants. Grass flats and forest scrubs are the primary terrestrial vegetation seen in the park while blue green algae dominates the aquatic parts

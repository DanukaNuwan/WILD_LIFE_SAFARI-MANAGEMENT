            <p>
            The Udawalawe National Park is situated covering areas in two Districts of Sri Lanka; namely Rathnapura and Monaragala. 
            This is the largest conservation area in both the districts and is home to the Walawe reservoir. Situated in the dry zone of Sri Lanka, the predominant vegetation type in the park is scattered grasslands and thorny shrubs. 
            The soil and climatic conditions are the key drivers of determining the major vegetation type in the park The period between May and September are special for park visitors as they get the opportunity to come across herds of elephants even during mid day. Even though the bird population in the park is significant, there observation can be difficult due to the tall grass that is present. In addition, over 20 species of mammals and many reptile species can be seen in the park

            <p>
            Kumana National Park is situated in the Eastern Province of Sri Lanka. The expansive Mangrove Swamp is its most prominent feature. The swamp is surrounded by plains and a jungle.  
            The park offers a complex mosaic of habitats with rocky outcrops breaking the continuous nature of the flat terrain. The water filled basins are abundant in water tolerant grasses and aquatic plants. Rainfall that determines the period of inundation primarily determines the floral composition of the water bodies. 
            In areas such as the peripheries where wet conditions are brief, the presence of creeping grasses can be seen. Kumana is celebrated around the country for its presence of a wide variety of avifauna including a number of species that visit the swamp for nesting during May. The black necked stork, one of Sri Lankas rarest birds can be observed here.

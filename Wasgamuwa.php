            <p>
            Wasgamuwa National Park situated 225km from the capital city Colombo is reputed as the best habitat in Sri Lanka for observing sloth bears. The density of bears in Wasgamuwa has been recorded to be higher than in any other area within the country. The name of the park is also believed to have derived from the meaning “Gathering of the Bears”. The quartzite Sudukanda range is the dominant topographic feature in the park that houses rocks that are primarily Pre-Cambrian. The park is contiguous with the Riverine Nature Reserve and has a climatic condition which is typical to the dry zone of Sri Lanka. Tropical dry mixed evergreen forests form the main floral component in the park even though other sub-natural vegetation types are also common mainly attributing the human intervention. 
            The faunal composition in the park consists of 23 species of mammals including sloth bears and Asian elephants, 143 species of birds, 15 species of amphibians, 52 butterfly species and 35 reptiles. Many endemics are also included. Another unique aspect of Wasgamuwa is that its home to the largest of Sri Lanka’s elephants that are knows as marsh elephants.

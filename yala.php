            <p>
            Yala National Park is situated in the south-east region of Sri Lanka and is the 2nd largest National Park in the island, situated some 300 km away from Colombo. It was at first established in the early 1890s as a game sanctuary . The park is located in the dry-zone region where the drought season is very long .The day time average temperature is over 30 degrees which is not uncommon in the region. 
            The parkland makes up most of the reserve but also includes lakes, beaches, jungle, rivers and scrubland. This variety in habitats provides an excellent range in wildlife. The largest concentration of Leopards can be seen in this region though the chances of seeing this animal are very low, and the creature is said to be one of the most endangered species. About 32 species of mammals, 125 species of birds and many reptiles and lagoon fauna species have been recorded in the park. 
            The Yala National Park is famous among visitors as the best place to view large mammals within one territory.
